package Demo44;

public class Demo44 {
    public static void main(String[] args) {
        /*
         * 接口中成员的特点
         * 成员变量：
         *  只能是常量
         *  默认修饰符public static final
         * 构造方法：没有
         * 成员方法：
         *  只能是抽象方法
         *  默认修饰符public abstract
         * JDK7以前：接口中只能定义抽象方法
         */

         /*
          * 类和类之间是继承关系，只能单继承、不能多继承
          * 类和接口是实现关系，一个类可以实现多个接口
          * 接口和接口是继承关系，能单继承和多继承
          */
    }
}
